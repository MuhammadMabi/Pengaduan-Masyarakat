     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $get_user = User::orderBy('role', 'ASC');

        if (auth()->user()->role == 'Petugas') {
            $user = $get_user->where('role', 'Warga')->get();
        }else {
            $user = $get_user->get();
        }

        return view('user.index', compact('user'));
    }

